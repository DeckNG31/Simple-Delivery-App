    private void guardarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_guardarBtnActionPerformed

        try {

            if (nombreInput.getText().equals("")
                    || descripcionInput.getText().equals("")
                    || precioInput.getText().equals("")
                    || pesoInput.getText().equals("")
                    || volumenInput.getText().equals("")
                    || alcoholInput.getText().equals("")) {

                throw new Exception("Llena todo lcdtm");
            }

         
          
            //obtener checkbox
            boolean aptoVegano = veganoBox.isSelected();
            
            int vendedorId= AutenticacionController.getInstance().obtenerVendedorAutenticado().getId();
            imc.editarBebida(itemMenuId, nombreInput.getText(), descripcionInput.getText(), precioInput.getText(), aptoVegano, pesoInput.getText(), volumenInput.getText(), alcoholInput.getText(), vendedorId);

            //vuelve
            HelpersVista.cambiarVentana(this, ListaItemMenuVista.class);
        } catch (Exception e) {
            HelpersVista.mostrarMensaje(e.getMessage(), "Error", "Alerta");
        }
    }//GEN-LAST:event_guardarBtnActionPerformed

