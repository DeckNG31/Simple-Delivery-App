    public EditarBebidaVista(Integer id) {
        itemMenuId = id;
        vc = VendedorController.getInstance();
        imc = ItemMenuController.getInstance();
        initComponents();

        Bebida b = (Bebida) imc.buscarItemMenuPorId(itemMenuId);
        vendedorId = b.getVendedorId();

        //insertar en fields
        nombreInput.setText(b.getNombre());
        descripcionInput.setText(b.getDescripcion());
        precioInput.setText(b.getPrecio().toString());
        pesoInput.setText(b.getPeso().toString());
        volumenInput.setText(b.getVolumen().toString());
        alcoholInput.setText(b.getGraduacionAlcohol().toString());
        veganoBox.setSelected(b.aptoVegano());

     
    }

 

